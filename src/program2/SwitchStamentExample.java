package program2;

import java.util.Scanner;

public class SwitchStamentExample {

	public static void main(String[] args) {
		
		int Rollnumber;
		System.out.println("Welcome to Java Team....");
		System.out.println("enter Roll number in between 1 to 5");
		Scanner sc = new Scanner(System.in);
		Rollnumber=sc.nextInt();
		
		switch(Rollnumber) {
		case 1:
			System.out.println("Akash");
			break;
		case 2:
			System.out.println("Harshal");
			break;
		case 3:
			System.out.println("Rupali");
			break;
		case 4:
			System.out.println("priyanka");
			break;
		case 5:
			System.out.println("sagar");
			break;
		default:
			System.out.println("please enter valid roll number...");
		
		
		
	
		
		
		
		

		
	}

		
	}

}
